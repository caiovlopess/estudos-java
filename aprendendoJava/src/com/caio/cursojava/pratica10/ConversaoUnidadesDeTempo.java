package com.caio.cursojava.pratica10;

public class ConversaoUnidadesDeTempo {
	public static double minutesToSeconds(double num) {
		return num * 60;
	}
	
	public static double hourToMinutes(double num) {
		return num * 60;
	}
	
	public static double dayToHours(double num) {
		return num * 24;
	}
	
	public static double weeksToDays(double num) {
		return num * 7;
	}
	
	public static double monthToDays(double num) {
		return num * 30;
	}
	
	public static double yearsToDays(double num) {
		return num * 365.25;
	}
}
